// ---- llm.LLMResponseChunk implementation ----

// openaiChatLLMResponseChunk implements llm.LLMResponseChunk for the OpenAI streaming API.
type openaiChatLLMResponseChunk struct {
	raw openaiChatChunk
}

func (c *openaiChatLLMResponseChunk) GetID() string    { return c.raw.ID }
func (c *openaiChatLLMResponseChunk) GetModel() string { return c.raw.Model }

func (c *openaiChatLLMResponseChunk) GetMessages() []llm.LLMMessage {
	if len(c.raw.Choices) == 0 {
		return nil
	}
	msgs := make([]llm.LLMMessage, 0, len(c.raw.Choices))
	for _, choice := range c.raw.Choices {
		msg := openaiChatDeltaToLLMMessage(choice.Delta)
		idx := choice.Index
		msg.Index = &idx
		msgs = append(msgs, *msg)
	}
	return msgs
}

func (c *openaiChatLLMResponseChunk) GetStopReason() string {
	if len(c.raw.Choices) == 0 || c.raw.Choices[0].FinishReason == nil {
		return ""
	}
	return *c.raw.Choices[0].FinishReason
}

func (c *openaiChatLLMResponseChunk) GetUsage() llm.LLMUsage {
	if c.raw.Usage == nil {
		return llm.LLMUsage{}
	}
	return openaiChatUsageToLLM(*c.raw.Usage)
}

// ToEvent encodes the chunk as a complete OpenAI SSE event: "data: <json>\n\n".
func (c *openaiChatLLMResponseChunk) ToEvent() ([]byte, error) {
	data, err := json.Marshal(c.raw)
	if err != nil {
		return nil, err
	}
	return append(append([]byte("data: "), data...), '\n', '\n'), nil
}

// parseOpenAIChunk parses a single data payload from an OpenAI streaming SSE response.
func parseOpenAIChunk(data []byte) (llm.LLMResponseChunk, error) {
	var chunk openaiChatChunk
	if err := json.Unmarshal(data, &chunk); err != nil {
		return nil, err
	}
	return &openaiChatLLMResponseChunk{raw: chunk}, nil
}

// ---- SSE parser ----

var openaiChatSSEDataPrefix = []byte("data: ")

// openaiChatSSEParser accumulates an OpenAI streaming SSE response into a final llm.LLMResponse.
type openaiChatSSEParser struct {
	buf   []byte
	done  bool
	id    string
	model string
	usage llm.LLMUsage
}

func newOpenAISSEParser() *openaiChatSSEParser {
	return &openaiChatSSEParser{}
}

func (a *openaiChatSSEParser) Feed(data []byte) ([]llm.LLMResponseChunk, error) {
	if a.done {
		return nil, nil
	}
	a.buf = append(a.buf, data...)
	return a.parseEvents()
}

func (a *openaiChatSSEParser) parseEvents() ([]llm.LLMResponseChunk, error) {
	var chunks []llm.LLMResponseChunk
	for {
		idx := bytes.IndexByte(a.buf, '\n')
		if idx < 0 {
			return chunks, nil
		}
		line := bytes.TrimSpace(a.buf[:idx])
		a.buf = a.buf[idx+1:]

		if !bytes.HasPrefix(line, openaiChatSSEDataPrefix) {
			continue
		}
		payload := bytes.TrimPrefix(line, openaiChatSSEDataPrefix)
		if bytes.Equal(payload, []byte("[DONE]")) {
			a.done = true
			return chunks, nil
		}
		chunk, err := parseOpenAIChunk(payload)
		if err != nil {
			return chunks, fmt.Errorf("llm-proxy: failed to parse OpenAI streaming chunk: %w", err)
		}
		if a.id == "" {
			a.id = chunk.GetID()
		}
		if a.model == "" {
			a.model = chunk.GetModel()
		}
		if u := chunk.GetUsage(); u != (llm.LLMUsage{}) {
			a.usage = u
		}
		chunks = append(chunks, chunk)
	}
}

func (a *openaiChatSSEParser) Finish() (llm.LLMResponse, error) {
	return &openaiChatLLMResponse{raw: openaiChatResponse{
		ID:    a.id,
		Model: a.model,
		Usage: openaiChatUsage{
			PromptTokens:     a.usage.InputTokens,
			CompletionTokens: a.usage.OutputTokens,
			TotalTokens:      a.usage.TotalTokens,
		},
	}}, nil
}

// ---- Factory ----

// openaiChatFactory implements llm.LLMFactory for the OpenAI Chat Completions API.
type openaiChatFactory struct{}

func (f *openaiChatFactory) ParseRequest(body []byte) (llm.LLMRequest, error) {
	return parseOpenAIRequest(body)
}

func (f *openaiChatFactory) ParseResponse(body []byte) (llm.LLMResponse, error) {
	return parseOpenAIResponse(body)
}

func (f *openaiChatFactory) ParseChunk(data []byte) (llm.LLMResponseChunk, error) {
	return parseOpenAIChunk(data)
}

func (f *openaiChatFactory) NewSSEParser() llm.SSEParser { return newOpenAISSEParser() }

func (f *openaiChatFactory) TransformRequest(req llm.LLMRequest) (llm.LLMRequest, error) {
	msgs := llmMsgsToOpenAI(req.GetMessages())
	tools := llmToolsToOpenAI(req.GetTools())
	raw := openaiChatRequest{
		Model:       req.GetModel(),
		Messages:    msgs,
		Stream:      req.IsStream(),
		MaxTokens:   req.GetMaxTokens(),
		Temperature: req.GetTemperature(),
		TopP:        req.GetTopP(),
	}
	if len(tools) > 0 {
		raw.Tools = tools
		raw.ToolChoice = llmToolChoiceToOpenAI(req.GetToolChoice())
	}
	return &openaiChatLLMRequest{raw: raw}, nil
}

func (f *openaiChatFactory) TransformResponse(resp llm.LLMResponse) (llm.LLMResponse, error) {
	msgs := resp.GetMessages()
	stopReason := resp.GetStopReason()
	choices := make([]openaiChatChoice, 0, len(msgs))
	for i, m := range msgs {
		oaMsg := openaiChatMessage{Role: m.Role}
		// Concatenate all text content parts into a plain string (OpenAI wire format).
		var textParts []string
		for _, part := range m.Content {
			if part.Type == llm.ContentPartTypeText {
				textParts = append(textParts, part.Text)
			}
		}
		if len(textParts) > 0 {
			s := strings.Join(textParts, "")
			oaMsg.Content = &s
		}
		if len(m.ToolCalls) > 0 {
			oaMsg.ToolCalls = llmToolCallsToOpenAI(m.ToolCalls)
		}
		choices = append(choices, openaiChatChoice{
			Index:        i,
			Message:      oaMsg,
			FinishReason: stopReason,
		})
	}
	u := resp.GetUsage()
	raw := openaiChatResponse{
		ID:    resp.GetID(),
		Model: resp.GetModel(),
		Usage: openaiChatUsage{
			PromptTokens:     u.InputTokens,
			CompletionTokens: u.OutputTokens,
			TotalTokens:      u.TotalTokens,
		},
		Choices: choices,
	}
	return &openaiChatLLMResponse{raw: raw}, nil
}

func (f *openaiChatFactory) TransformChunk(chunk llm.LLMResponseChunk) (llm.LLMResponseChunk, error) {
	raw := openaiChatChunk{
		ID:    chunk.GetID(),
		Model: chunk.GetModel(),
	}
	msgs := chunk.GetMessages()
	finishReason := chunk.GetStopReason()
	var choices []openaiChatChunkChoice
	if len(msgs) > 0 {
		for _, msg := range msgs {
			oaDelta := openaiChatDelta{Role: msg.Role}
			for _, part := range msg.Content {
				if part.Type == llm.ContentPartTypeText && part.Text != "" {
					s := part.Text
					oaDelta.Content = &s
					break
				}
			}
			if len(msg.ToolCalls) > 0 {
				oaDelta.ToolCalls = llmToolCallsToOpenAI(msg.ToolCalls)
			}
			idx := 0
			if msg.Index != nil {
				idx = *msg.Index
			}
			choice := openaiChatChunkChoice{Index: idx, Delta: oaDelta}
			if finishReason != "" {
				choice.FinishReason = &finishReason
			}
			choices = append(choices, choice)
		}
	} else {
		choice := openaiChatChunkChoice{Index: 0}
		if finishReason != "" {
			choice.FinishReason = &finishReason
		}
		choices = []openaiChatChunkChoice{choice}
	}
	raw.Choices = choices
	u := chunk.GetUsage()
	if u != (llm.LLMUsage{}) {
		raw.Usage = &openaiChatUsage{
			PromptTokens:     u.InputTokens,
			CompletionTokens: u.OutputTokens,
			TotalTokens:      u.TotalTokens,
		}
	}
	return &openaiChatLLMResponseChunk{raw: raw}, nil
}

