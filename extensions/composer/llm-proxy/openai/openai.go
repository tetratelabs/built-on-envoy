// openaiChatAnyToToolChoice converts an OpenAI tool_choice value (decoded from JSON
// as any) to the canonical llm.LLMToolChoice. OpenAI uses strings ("auto", "none",
// "required") or {"type":"function","function":{"name":"..."}} objects.
// The object form is normalised to Type "tool" so the canonical representation
// is provider-agnostic.
func openaiChatAnyToToolChoice(v any) *llm.LLMToolChoice {
	switch tc := v.(type) {
	case string:
		if tc == "" {
			return nil
		}
		return &llm.LLMToolChoice{Type: tc}
	case map[string]any:
		// OpenAI's specific-tool object has type "function"; normalise to "tool".
		if strVal(tc["type"]) == llm.ToolTypeFunction {
			choice := &llm.LLMToolChoice{Type: llm.ToolChoiceFunction}
			if fn, ok := tc["function"].(map[string]any); ok {
				choice.Name = strVal(fn["name"])
			}
			return choice
		}
		return &llm.LLMToolChoice{Type: strVal(tc["type"])}
	}
	return nil
}

// llmToolChoiceToOpenAI converts a canonical llm.LLMToolChoice to the OpenAI wire format.
// Returns "auto", "none", "required" as strings; specific-tool as a map.
func llmToolChoiceToOpenAI(tc *llm.LLMToolChoice) any {
	if tc == nil {
		return nil
	}
	switch tc.Type {
	case llm.ToolChoiceFunction:
		return map[string]any{
			"type":     llm.ToolTypeFunction,
			"function": map[string]any{"name": tc.Name},
		}
	default:
		return tc.Type
	}
}

