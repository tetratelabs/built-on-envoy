// openaiChatMessagesToLLM converts OpenAI request messages to the canonical llm.LLMMessage slice.
func openaiChatMessagesToLLM(msgs []openaiChatMessageParam) []llm.LLMMessage {
	result := make([]llm.LLMMessage, 0, len(msgs))
	for _, msg := range msgs {
		llmMsg := llm.LLMMessage{Role: msg.Role, ToolCallID: msg.ToolCallID}

		// Decode tool calls from the top-level field into the canonical message.
		for i := range msg.ToolCalls {
			tc := &msg.ToolCalls[i]
			llmMsg.ToolCalls = append(llmMsg.ToolCalls, llm.LLMToolCall{
				ID:        tc.ID,
				Type:      tc.Type,
				Name:      tc.Function.Name,
				Arguments: tc.Function.Arguments,
			})
		}

		// Populate content parts by type-switching on the typed Content value.
		switch c := msg.Content.(type) {
		case string:
			if c != "" {
				llmMsg.Content = append(llmMsg.Content, llm.LLMContentPart{Type: llm.ContentPartTypeText, Text: c})
			}
		case []any:
			for _, part := range c {
				switch p := part.(type) {
				case *openaiChatContentPartText:
					llmMsg.Content = append(llmMsg.Content, llm.LLMContentPart{Type: llm.ContentPartTypeText, Text: p.Text})
				case *openaiChatContentPartImageURL:
					llmMsg.Content = append(llmMsg.Content, llm.LLMContentPart{Type: llm.ContentPartTypeImage, Image: &llm.LLMContentImage{URL: p.ImageURL.URL}})
				case *openaiChatContentPartInputAudio:
					llmMsg.Content = append(llmMsg.Content, llm.LLMContentPart{
						Type:  llm.ContentPartTypeAudio,
						Audio: &llm.LLMContentAudio{Data: p.InputAudio.Data, Format: p.InputAudio.Format},
					})
				case *openaiChatContentPartRefusal:
					llmMsg.Content = append(llmMsg.Content, llm.LLMContentPart{Type: llm.ContentPartTypeRefusal, Text: p.Refusal})
				case *openaiChatContentPartFile:
					llmMsg.Content = append(llmMsg.Content, llm.LLMContentPart{
						Type: llm.ContentPartTypeFile,
						File: &llm.LLMContentFile{FileID: p.File.FileID, FileData: p.File.FileData, Filename: p.File.Filename},
					})
				}
			}
		}

		result = append(result, llmMsg)
	}
	return result
}

// openaiChatToolsToLLM converts OpenAI tool definitions to canonical llm.LLMTool values.
func openaiChatToolsToLLM(tools []openaiChatTool) []llm.LLMTool {
	result := make([]llm.LLMTool, 0, len(tools))
	for _, t := range tools {
		result = append(result, llm.LLMTool{
			Type:        llm.ToolTypeFunction,
			Name:        t.Function.Name,
			Description: t.Function.Description,
			Parameters:  t.Function.Parameters,
		})
	}
	return result
}

// openaiChatChoicesToLLMMessages converts OpenAI choices to canonical llm.LLMMessage values.
func openaiChatChoicesToLLMMessages(choices []openaiChatChoice) []llm.LLMMessage {
	result := make([]llm.LLMMessage, 0, len(choices))
	for _, c := range choices {
		msg := llm.LLMMessage{Role: c.Message.Role}
		if c.Message.Content != nil && *c.Message.Content != "" {
			msg.Content = []llm.LLMContentPart{{Type: llm.ContentPartTypeText, Text: *c.Message.Content}}
		}
		if c.Message.Refusal != nil && *c.Message.Refusal != "" {
			msg.Content = append(msg.Content, llm.LLMContentPart{Type: llm.ContentPartTypeRefusal, Text: *c.Message.Refusal})
		}
		for _, tc := range c.Message.ToolCalls {
			msg.ToolCalls = append(msg.ToolCalls, llm.LLMToolCall{
				ID:        tc.ID,
				Type:      tc.Type,
				Name:      tc.Function.Name,
				Arguments: tc.Function.Arguments,
			})
		}
		result = append(result, msg)
	}
	return result
}

// openaiChatDeltaToLLMMessage converts an OpenAI streaming delta to a canonical llm.LLMMessage.
func openaiChatDeltaToLLMMessage(delta openaiChatDelta) *llm.LLMMessage {
	msg := &llm.LLMMessage{Role: delta.Role}
	if delta.Content != nil && *delta.Content != "" {
		msg.Content = []llm.LLMContentPart{{Type: llm.ContentPartTypeText, Text: *delta.Content}}
	}
	for _, tc := range delta.ToolCalls {
		msg.ToolCalls = append(msg.ToolCalls, llm.LLMToolCall{
			ID:        tc.ID,
			Type:      tc.Type,
			Name:      tc.Function.Name,
			Arguments: tc.Function.Arguments,
		})
	}
	return msg
}

// openaiChatUsageToLLM converts an openaiChatUsage to a canonical llm.LLMUsage.
func openaiChatUsageToLLM(u openaiChatUsage) llm.LLMUsage {
	return llm.LLMUsage{
		InputTokens:  u.PromptTokens,
		OutputTokens: u.CompletionTokens,
		TotalTokens:  u.TotalTokens,
	}
}

// llmToolCallsToOpenAI converts canonical llm.LLMToolCall values to openaiChatToolCall values.
func llmToolCallsToOpenAI(calls []llm.LLMToolCall) []openaiChatToolCall {
	result := make([]openaiChatToolCall, 0, len(calls))
	for _, c := range calls {
		tc := llm.ToolTypeFunction
		if c.Type != "" {
			tc = c.Type
		}
		call := openaiChatToolCall{ID: c.ID, Type: tc}
		call.Function.Name = c.Name
		call.Function.Arguments = c.Arguments
		result = append(result, call)
	}
	return result
}

// llmMsgsToOpenAI converts canonical llm.LLMMessage values to OpenAI request message params.
func llmMsgsToOpenAI(msgs []llm.LLMMessage) []openaiChatMessageParam {
	result := make([]openaiChatMessageParam, 0, len(msgs))
	for _, msg := range msgs {
		oaMsg := openaiChatMessageParam{Role: msg.Role, ToolCallID: msg.ToolCallID}

		// Tool calls from the top-level field become the wire tool_calls array.
		if len(msg.ToolCalls) > 0 {
			oaMsg.ToolCalls = llmToolCallsToOpenAI(msg.ToolCalls)
		}

		var parts []any
		for _, part := range msg.Content {
			switch part.Type {
			case llm.ContentPartTypeRefusal:
				parts = append(parts, &openaiChatContentPartRefusal{Type: openaiChatContentTypeRefusal, Refusal: part.Text})
			case llm.ContentPartTypeImage:
				img := &openaiChatContentPartImageURL{Type: openaiChatContentTypeImageURL}
				if part.Image != nil {
					img.ImageURL.URL = part.Image.URL
				}
				parts = append(parts, img)
			case llm.ContentPartTypeAudio:
				audio := &openaiChatContentPartInputAudio{Type: openaiChatContentTypeInputAudio}
				if part.Audio != nil {
					audio.InputAudio.Data = part.Audio.Data
					audio.InputAudio.Format = part.Audio.Format
				}
				parts = append(parts, audio)
			case llm.ContentPartTypeFile:
				f := &openaiChatContentPartFile{Type: openaiChatContentTypeFile}
				if part.File != nil {
					f.File.FileID = part.File.FileID
					f.File.FileData = part.File.FileData
					f.File.Filename = part.File.Filename
				}
				parts = append(parts, f)
			default:
				parts = append(parts, &openaiChatContentPartText{Type: openaiChatContentTypeText, Text: part.Text})
			}
		}

		// Encode content: plain string for a single text part, typed array otherwise.
		if len(parts) == 1 {
			if t, ok := parts[0].(*openaiChatContentPartText); ok {
				oaMsg.Content = t.Text
			} else {
				oaMsg.Content = parts
			}
		} else if len(parts) > 0 {
			oaMsg.Content = parts
		}

		result = append(result, oaMsg)
	}
	return result
}

// llmToolsToOpenAI converts canonical llm.LLMTool values to OpenAI tool definitions.
func llmToolsToOpenAI(tools []llm.LLMTool) []openaiChatTool {
	result := make([]openaiChatTool, 0, len(tools))
	for _, t := range tools {
		tool := openaiChatTool{Type: llm.ToolTypeFunction}
		tool.Function.Name = t.Name
		tool.Function.Description = t.Description
		tool.Function.Parameters = t.Parameters
		result = append(result, tool)
	}
	return result
}

