// strVal safely casts v to string, returning "" on failure.
func strVal(v any) string {
	s, _ := v.(string)
	return s
}

// OpenAI content part type constants.
const (
	openaiChatContentTypeText       = "text"
	openaiChatContentTypeImageURL   = "image_url"
	openaiChatContentTypeInputAudio = "input_audio"
	openaiChatContentTypeFile       = "file"
	openaiChatContentTypeRefusal    = "refusal"
)

// openaiChatUsage holds token-usage statistics returned in every response and
// in the final streaming chunk (when stream_options.include_usage is true).
type openaiChatUsage struct {
	PromptTokens     uint32 `json:"prompt_tokens"`     // tokens consumed by the input messages
	CompletionTokens uint32 `json:"completion_tokens"` // tokens generated by the model
	TotalTokens      uint32 `json:"total_tokens"`      // sum of prompt_tokens and completion_tokens
}

// openaiChatContentPartText is a plain-text content part used inside a message
// content array (type discriminator = "text").
type openaiChatContentPartText struct {
	Type string `json:"type"` // always "text"
	Text string `json:"text"` // the text content
}

// openaiChatContentPartImageURL is an image content part used inside a message
// content array (type discriminator = "image_url").
// URL accepts an https URL or a base64 data URI (data:image/...;base64,...).
// Detail controls the resolution used for vision processing; omit to let the
// model choose automatically.
type openaiChatContentPartImageURL struct {
	Type     string `json:"type"` // always "image_url"
	ImageURL struct {
		URL    string `json:"url"`
		Detail string `json:"detail,omitempty"` // resolution hint: "auto" | "low" | "high"
	} `json:"image_url"`
}

// openaiChatContentPartInputAudio is an audio content part used inside a message
// content array (type discriminator = "input_audio").
type openaiChatContentPartInputAudio struct {
	Type       string `json:"type"` // always "input_audio"
	InputAudio struct {
		Data   string `json:"data"`   // base64-encoded audio bytes
		Format string `json:"format"` // encoding format: "wav" | "mp3"
	} `json:"input_audio"`
}

// openaiChatContentPartFile is a file content part used inside a message content
// array (type discriminator = "file").
// Supply exactly one of FileID (a file previously uploaded via the Files API) or
// FileData (inline base64-encoded bytes). Filename guides MIME-type inference.
type openaiChatContentPartFile struct {
	Type string `json:"type"` // always "file"
	File struct {
		FileID   string `json:"file_id,omitempty"`   // ID of a file uploaded via the Files API
		FileData string `json:"file_data,omitempty"` // inline base64-encoded file bytes
		Filename string `json:"filename,omitempty"`  // filename hint used for MIME-type detection
	} `json:"file"`
}

// openaiChatContentPartRefusal is a refusal content part used inside a message
// content array (type discriminator = "refusal").
type openaiChatContentPartRefusal struct {
	Type    string `json:"type"`    // always "refusal"
	Refusal string `json:"refusal"` // the refusal text produced by the model
}

// openaiChatMessageParamAudio is the audio reference used inside an assistant
// message param when replaying a prior audio response in a multi-turn conversation.
// Only the ID is needed; the server resolves the full audio object from it.
type openaiChatMessageParamAudio struct {
	ID string `json:"id"` // ID from a prior openaiChatMessageAudio response
}

// openaiChatMessageParam is one message in the request messages array.
// Role must be one of "system", "developer", "user", "assistant", or "tool".
// "developer" replaces "system" on newer models for high-priority instructions.
//
// Content holds one of:
//   - string  – plain-text shorthand accepted for all roles
//   - []any   – array of typed content-part pointers for multimodal input:
//     |         *openaiChatContentPartText
//     |         *openaiChatContentPartImageURL
//     |         *openaiChatContentPartInputAudio
//     |         *openaiChatContentPartRefusal
//     |         *openaiChatContentPartFile
//   - nil     – omitted; valid on assistant messages that only carry ToolCalls
//
// Refusal and Audio are only valid on assistant-role messages and must mirror
// the values returned by a prior response when replaying conversation history.
// ToolCalls is only set on assistant-role messages when replaying prior model output.
// ToolCallID is only set on tool-role messages; it must match the ID in the
// corresponding assistant ToolCalls entry.
// Name optionally distinguishes participants that share the same role.
type openaiChatMessageParam struct {
	Name       string                       // optional participant name to distinguish senders with the same role
	Role       string                       // "system" | "developer" | "user" | "assistant" | "tool"
	Content    any                          // string, []any of content-part pointers, or nil
	ToolCallID string                       // ID of the tool call this message is answering (tool role only)
	Refusal    *string                      // refusal text from a prior assistant response (assistant role only)
	Audio      *openaiChatMessageParamAudio // reference to a prior audio response by ID (assistant role only)
	ToolCalls  []openaiChatToolCall         // tool invocations to replay (assistant role only)
}

func (m *openaiChatMessageParam) UnmarshalJSON(data []byte) error {
	var raw struct {
		Role       string                       `json:"role"`
		Content    any                          `json:"content,omitempty"`
		Refusal    *string                      `json:"refusal,omitempty"`
		Audio      *openaiChatMessageParamAudio `json:"audio,omitempty"`
		ToolCalls  []openaiChatToolCall         `json:"tool_calls,omitempty"`
		ToolCallID string                       `json:"tool_call_id,omitempty"`
		Name       string                       `json:"name,omitempty"`
	}
	if err := json.Unmarshal(data, &raw); err != nil {
		return err
	}
	m.Role = raw.Role
	m.Refusal = raw.Refusal
	m.Audio = raw.Audio
	m.ToolCalls = raw.ToolCalls
	m.ToolCallID = raw.ToolCallID
	m.Name = raw.Name
	switch c := raw.Content.(type) {
	case string:
		m.Content = c
	case []interface{}:
		parts := make([]any, 0, len(c))
		for _, item := range c {
			part, err := unmarshalOpenAIContentPart(item)
			if err != nil {
				return err
			}
			parts = append(parts, part)
		}
		m.Content = parts
	}
	return nil
}

func (m openaiChatMessageParam) MarshalJSON() ([]byte, error) {
	raw := struct {
		Role       string                       `json:"role"`
		Content    any                          `json:"content,omitempty"`
		Refusal    *string                      `json:"refusal,omitempty"`
		Audio      *openaiChatMessageParamAudio `json:"audio,omitempty"`
		ToolCalls  []openaiChatToolCall         `json:"tool_calls,omitempty"`
		ToolCallID string                       `json:"tool_call_id,omitempty"`
		Name       string                       `json:"name,omitempty"`
	}{
		Role:       m.Role,
		Content:    m.Content,
		Refusal:    m.Refusal,
		Audio:      m.Audio,
		ToolCalls:  m.ToolCalls,
		ToolCallID: m.ToolCallID,
		Name:       m.Name,
	}
	return json.Marshal(raw)
}

// openaiChatMessageAudio holds the audio output embedded in a response message.
// Present only when the request included "audio" in the modalities list.
type openaiChatMessageAudio struct {
	ID         string `json:"id"`         // unique identifier for this audio output; used to reference it in follow-up turns
	Data       string `json:"data"`       // base64-encoded audio bytes
	ExpiresAt  int64  `json:"expires_at"` // Unix timestamp after which the server deletes the audio; cannot be referenced after this
	Transcript string `json:"transcript"` // text transcript of the generated audio
}

// openaiChatMessage is the assistant message returned inside a response choice.
// Role is always "assistant".
// Content contains the generated text, or nil when the response is tool calls
// only or an audio-only response (the wire value is null in those cases).
// Refusal is non-nil when the model declined to respond; it carries the refusal text.
// Audio is non-nil when an audio output modality was requested.
// ToolCalls lists the function invocations the model wants to perform.
type openaiChatMessage struct {
	Role      string                  `json:"role"`                 // always "assistant"
	Content   *string                 `json:"content"`              // generated text, or null when tool_calls or audio is the sole output
	Refusal   *string                 `json:"refusal,omitempty"`    // refusal message when the model declined to respond
	Audio     *openaiChatMessageAudio `json:"audio,omitempty"`      // audio output when the "audio" modality was requested
	ToolCalls []openaiChatToolCall    `json:"tool_calls,omitempty"` // function invocations the model is requesting
}

// unmarshalOpenAIContentPart converts a decoded JSON value (map[string]any)
// into a typed content-part pointer by inspecting the "type" field directly.
func unmarshalOpenAIContentPart(v any) (any, error) {
	m, ok := v.(map[string]any)
	if !ok {
		return nil, fmt.Errorf("openai: content part must be a JSON object, got %T", v)
	}
	t, _ := m["type"].(string)
	switch t {
	case openaiChatContentTypeText:
		p := &openaiChatContentPartText{Type: openaiChatContentTypeText}
		p.Text, _ = m["text"].(string)
		return p, nil
	case openaiChatContentTypeImageURL:
		p := &openaiChatContentPartImageURL{Type: openaiChatContentTypeImageURL}
		img, _ := m["image_url"].(map[string]any)
		p.ImageURL.URL, _ = img["url"].(string)
		p.ImageURL.Detail, _ = img["detail"].(string)
		return p, nil
	case openaiChatContentTypeInputAudio:
		p := &openaiChatContentPartInputAudio{Type: openaiChatContentTypeInputAudio}
		audio, _ := m["input_audio"].(map[string]any)
		p.InputAudio.Data, _ = audio["data"].(string)
		p.InputAudio.Format, _ = audio["format"].(string)
		return p, nil
	case openaiChatContentTypeRefusal:
		p := &openaiChatContentPartRefusal{Type: openaiChatContentTypeRefusal}
		p.Refusal, _ = m["refusal"].(string)
		return p, nil
	case openaiChatContentTypeFile:
		p := &openaiChatContentPartFile{Type: openaiChatContentTypeFile}
		f, _ := m["file"].(map[string]any)
		p.File.FileID, _ = f["file_id"].(string)
		p.File.FileData, _ = f["file_data"].(string)
		p.File.Filename, _ = f["filename"].(string)
		return p, nil
	default:
		// Unknown type: return the raw map so callers can inspect it.
		return m, nil
	}
}

// openaiChatToolCall is a tool invocation emitted by the model inside an
// assistant message. The ID must be echoed back as tool_call_id in the
// corresponding tool-role message so the model can match results to requests.
type openaiChatToolCall struct {
	ID       string `json:"id"`   // unique identifier for this invocation; echo as tool_call_id
	Type     string `json:"type"` // always "function"
	Function struct {
		Name      string `json:"name"`      // name of the function to call
		Arguments string `json:"arguments"` // model-generated JSON-encoded argument string
	} `json:"function"`
}

// openaiChatTool is a function tool definition included in the request.
// The model uses Description to decide when to invoke the tool and
// Parameters (a JSON Schema object) to generate correctly typed arguments.
type openaiChatTool struct {
	Type     string `json:"type"` // always "function"
	Function struct {
		Name        string `json:"name"`
		Description string `json:"description,omitempty"` // natural-language description shown to the model to guide tool selection
		Parameters  any    `json:"parameters,omitempty"`  // JSON Schema describing the function's accepted arguments
	} `json:"function"`
}

// openaiChatRequest is the OpenAI Chat Completions request body.
type openaiChatRequest struct {
	Model       string                   `json:"model"`                 // model ID, e.g. "gpt-4o"
	Messages    []openaiChatMessageParam `json:"messages"`              // ordered conversation history sent to the model
	Stream      bool                     `json:"stream,omitempty"`      // if true, responses are delivered as SSE chunks
	Tools       []openaiChatTool         `json:"tools,omitempty"`       // function tools the model may invoke
	ToolChoice  any                      `json:"tool_choice,omitempty"` // tool-selection policy: "auto" | "none" | "required" | {"type":"function","function":{"name":"..."}}
	MaxTokens   *int                     `json:"max_tokens,omitempty"`  // maximum output tokens; deprecated for reasoning models — prefer max_completion_tokens
	Temperature *float64                 `json:"temperature,omitempty"` // sampling temperature in [0, 2]; higher values produce more random output
	TopP        *float64                 `json:"top_p,omitempty"`       // nucleus-sampling probability cutoff in [0, 1]
}

// openaiChatResponse is the OpenAI Chat Completions non-streaming response body.
type openaiChatResponse struct {
	ID      string             `json:"id"`                // unique response identifier, prefixed "chatcmpl-"
	Object  string             `json:"object,omitempty"`  // always "chat.completion"
	Created int64              `json:"created,omitempty"` // Unix timestamp of when the response was created
	Model   string             `json:"model"`             // model that generated the response
	Choices []openaiChatChoice `json:"choices"`           // generated completions; one entry per n (default n=1)
	Usage   openaiChatUsage    `json:"usage"`             // token-usage statistics for the request
}

// openaiChatChoice is one generated completion inside a non-streaming response.
type openaiChatChoice struct {
	Index        int               `json:"index"`                   // 0-based completion index, corresponds to the n parameter
	Message      openaiChatMessage `json:"message"`                 // the assistant message generated for this choice
	FinishReason string            `json:"finish_reason,omitempty"` // reason generation stopped: "stop" | "length" | "tool_calls" | "content_filter"
}

// openaiChatChunk is a single SSE data event in an OpenAI streaming response.
// All chunks share the same ID. Usage is nil on every chunk except the last,
// and only when stream_options.include_usage was set to true in the request.
type openaiChatChunk struct {
	ID      string                  `json:"id"`                // shared identifier across all chunks of one response
	Object  string                  `json:"object,omitempty"`  // always "chat.completion.chunk"
	Created int64                   `json:"created,omitempty"` // Unix timestamp of when the stream started
	Model   string                  `json:"model,omitempty"`   // model producing the stream
	Choices []openaiChatChunkChoice `json:"choices"`           // incremental choice updates; usually one entry
	Usage   *openaiChatUsage        `json:"usage,omitempty"`   // token-usage statistics; only present on the final chunk
}

// openaiChatChunkChoice is one incremental choice update inside a streaming chunk.
type openaiChatChunkChoice struct {
	Index        int             `json:"index"`         // 0-based choice index
	Delta        openaiChatDelta `json:"delta"`         // incremental content update for this chunk
	FinishReason *string         `json:"finish_reason"` // null on all intermediate chunks; set to a stop reason on the final chunk
}

// openaiChatDelta carries the incremental content update inside a streaming chunk choice.
// Role is only populated on the very first chunk of a response.
// Content and ToolCalls are mutually exclusive within a single chunk.
type openaiChatDelta struct {
	Role      string               `json:"role,omitempty"`       // assistant role; only present on the first chunk
	Content   *string              `json:"content,omitempty"`    // incremental text token(s) for this chunk
	ToolCalls []openaiChatToolCall `json:"tool_calls,omitempty"` // incremental tool-call data; mutually exclusive with Content
}

